function mouseDown() {
    document.getElementsByClassName("div1").style.color = "blue";
  }

  function mouseUp() {
    document.getElementsByClassName("div1").style.color = "red";
  }

  function mouseover() {
      document.getElementsByClassName("div2").style.display = "none";
  }

  function mouseout() {
      document.getElementsByClassName("div2").style.display = "block";
  }